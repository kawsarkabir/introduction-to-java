public class Condition {
    public static void main(String[] args) {
        int number = -10;

        // If statement
        if (number > 0) {
            System.out.println("The number is positive.");
        } else {
            System.out.println("The number is non-positive.");
        }

        // For loop
        for (int i = 1; i <= 5; i++) {
            System.out.println("Iteration " + i);
        }
    }
}

